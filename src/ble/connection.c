////////////////////////////////////////////////////////////////////////////////
// Includes
////////////////////////////////////////////////////////////////////////////////

#include "connection.h"

/* Zephyr includes */
#include <bluetooth/bluetooth.h>
#include <bluetooth/conn.h>
#include <bluetooth/hci.h>
#include <stddef.h>
#include <sys/printk.h>
#include <sys/util.h>
#include <zephyr/types.h>

////////////////////////////////////////////////////////////////////////////////
// Private variables
////////////////////////////////////////////////////////////////////////////////

/* BLE connection */
static struct bt_conn *conn;

////////////////////////////////////////////////////////////////////////////////
// Private function declarations
////////////////////////////////////////////////////////////////////////////////

static void _connected(struct bt_conn *connected, uint8_t err);
static void _disconnected(struct bt_conn *disconn, uint8_t reason);

////////////////////////////////////////////////////////////////////////////////
// Type declarations
///////////////////////////////////////////////////////////////////////////////

static struct bt_conn_cb conn_callbacks = {
    .connected = _connected,
    .disconnected = _disconnected,
};

////////////////////////////////////////////////////////////////////////////////
// Public functions
////////////////////////////////////////////////////////////////////////////////

void connection_init() { bt_conn_cb_register(&conn_callbacks); }

////////////////////////////////////////////////////////////////////////////////
// Private functions
////////////////////////////////////////////////////////////////////////////////

static void _connected(struct bt_conn *connected, uint8_t err)
{
    if (err)
    {
        printk("Connection failed (err %u)", err);
    }
    else
    {
        printk("Connected");
        if (!conn)
        {
            conn = bt_conn_ref(connected);
        }
    }
}

static void _disconnected(struct bt_conn *disconn, uint8_t reason)
{
    if (conn)
    {
        bt_conn_unref(conn);
        conn = NULL;
    }

    printk("Disconnected (reason %u)", reason);
}